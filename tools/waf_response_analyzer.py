_INSECURE_CTX: ssl.SSLContext | None = None


def _insecure_ssl_context() -> ssl.SSLContext:
    global _INSECURE_CTX
    if _INSECURE_CTX is None:
        ctx = ssl.create_default_context()
        ctx.check_hostname = False
        ctx.verify_mode = ssl.CERT_NONE
        _INSECURE_CTX = ctx
    return _INSECURE_CTX


def _http_get(url: str, *, timeout: float = 8.0) -> dict[str, Any]:
    """Fetch a URL with stdlib only. Returns dict with status/body/headers/elapsed_ms."""
    req = urllib.request.Request(
        url,
        headers={
            "User-Agent": "Mozilla/5.0 (compatible; waf-response-analyzer/1.0)",
            "Accept": "*/*",
        },
    )
    started = datetime.now(timezone.utc)
    try:
        with urllib.request.urlopen(
            req, timeout=timeout, context=_insecure_ssl_context()
        ) as resp:
            raw = resp.read()
            body = raw.decode("utf-8", errors="replace")
            headers = "\n".join(f"{k}: {v}" for k, v in resp.headers.items())
            status = resp.status
    except urllib.error.HTTPError as e:
        try:
            raw = e.read() or b""
        except Exception:
            raw = b""
        body = raw.decode("utf-8", errors="replace")
        headers = "\n".join(f"{k}: {v}" for k, v in (e.headers or {}).items())
        status = e.code
    except (urllib.error.URLError, TimeoutError, ssl.SSLError, OSError) as e:
        body = ""
        headers = ""
        status = 0
        elapsed_ms = (datetime.now(timezone.utc) - started).total_seconds() * 1000.0
        return {
            "status": status,
            "body": body,
            "headers": headers,
            "elapsed_ms": elapsed_ms,
            "error": str(e),
        }
    elapsed_ms = (datetime.now(timezone.utc) - started).total_seconds() * 1000.0
    return {
        "status": status,
        "body": body,
        "headers": headers,
        "elapsed_ms": elapsed_ms,
    }


