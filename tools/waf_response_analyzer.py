HTML_TAG_RE = re.compile(r"<[^>]+>")
BUSINESS_SIGNAL_RE = re.compile(
    r"(?i)(?:csrf[_-]?token|api[_-]?key|user[_-]?id|access[_-]?token|"
    r"refresh[_-]?token|session[_-]?id|\"[a-z_]+\"\s*:\s*\"|<form\s)"
)
BUSINESS_COOKIE_RE = re.compile(
    r"(?i)set-cookie:\s*(?:[^\n=]*?)(?:session|sess|csrf|auth|jwt|sid|token|phpsessid|jsessionid)"
)


def _strip_html(body: str, max_len: int = 500) -> str:
    text = HTML_TAG_RE.sub(" ", body[:8000])
    text = re.sub(r"\s+", " ", text).strip()
    return text[:max_len]


@dataclasses.dataclass
class ResponseFingerprint:
    status_code: int
    body_length: int
    body_preview: str
    vendor_hits: list[str]
    log_ids: dict[str, str | None]
    has_business_signal: bool
    has_business_cookies: bool
    has_block_title: bool
    has_challenge_signal: bool
    response_time_ms: float
    body_sha256: str

    @classmethod
    def build(
        cls,
        body: str,
        headers: str,
        *,
        status_code: int,
        body_length: int,
        response_time_ms: float,
        db: WAFSignatureDB,
        extractor: LogIDExtractor,
    ) -> "ResponseFingerprint":
        vendor_hits = db.match_vendors(body, headers)
        log_ids = extractor.extract(body)
        return cls(
            status_code=status_code,
            body_length=body_length,
            body_preview=_strip_html(body),
            vendor_hits=vendor_hits,
            log_ids=log_ids,
            has_business_signal=bool(BUSINESS_SIGNAL_RE.search(body[:16000])),
            has_business_cookies=bool(BUSINESS_COOKIE_RE.search(headers)),
            has_block_title=db.has_block_title(body),
            has_challenge_signal=db.has_challenge_signal(body),
            response_time_ms=response_time_ms,
            body_sha256=hashlib.sha256(body.encode("utf-8", errors="replace")).hexdigest(),
        )


