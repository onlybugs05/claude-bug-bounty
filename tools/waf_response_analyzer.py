_TOKEN_RE = re.compile(r"[A-Za-z0-9_]{3,}")


def _tokens(text: str) -> set[str]:
    return set(_TOKEN_RE.findall(text.lower()))


def diff_bodies(a: str, b: str) -> dict[str, Any]:
    la, lb = len(a), len(b)
    length_diff = lb - la
    length_ratio = round(lb / la, 4) if la else 0.0

    a_lines = {ln.strip() for ln in a.splitlines() if ln.strip()}
    b_lines = {ln.strip() for ln in b.splitlines() if ln.strip()}
    common_lines = a_lines & b_lines
    total_lines = a_lines | b_lines
    line_overlap = round(len(common_lines) / max(len(total_lines), 1), 4)

    a_tokens = _tokens(a)
    b_tokens = _tokens(b)
    common_tokens = a_tokens & b_tokens
    union_tokens = a_tokens | b_tokens
    token_overlap = round(len(common_tokens) / max(len(union_tokens), 1), 4)

    return {
        "length_a": la,
        "length_b": lb,
        "length_diff": length_diff,
        "length_ratio_b_over_a": length_ratio,
        "line_overlap_jaccard": line_overlap,
        "token_overlap_jaccard": token_overlap,
        "sha256_a": hashlib.sha256(a.encode("utf-8", errors="replace")).hexdigest(),
        "sha256_b": hashlib.sha256(b.encode("utf-8", errors="replace")).hexdigest(),
        "identical": a == b,
    }


# ---------------------------------------------------------------------------
# I/O helpers
# ---------------------------------------------------------------------------


def _read_text(path: str | None) -> str:
    if not path:
        return ""
    if path == "/dev/null":
        return ""
    try:
        with open(path, "rb") as fh:
            data = fh.read()
        return data.decode("utf-8", errors="replace")
    except FileNotFoundError:
        return ""
    except OSError as e:
        print(f"[warn] could not read {path}: {e}", file=sys.stderr)
        return ""


def _load_baseline(path: str | None) -> dict[str, Any]:
    if not path:
        return EMPTY_BASELINE
    try:
        with open(path, "r", encoding="utf-8") as fh:
            data = json.load(fh)
        if not isinstance(data, dict):
            return EMPTY_BASELINE
        data.setdefault("block_baseline", EMPTY_BASELINE["block_baseline"])
        data.setdefault("normal_baseline", EMPTY_BASELINE["normal_baseline"])
        return data
    except (FileNotFoundError, json.JSONDecodeError):
        return EMPTY_BASELINE
    except OSError as e:
        print(f"[warn] could not load baseline {path}: {e}", file=sys.stderr)
        return EMPTY_BASELINE


def _parse_metrics(metrics: str | None) -> tuple[int, int, float]:
    """Parse "code|size|time" with safe defaults."""
    if not metrics:
        return 0, 0, 0.0
    parts = metrics.split("|")
    try:
        code = int(parts[0]) if len(parts) > 0 and parts[0] else 0
    except ValueError:
        code = 0
    try:
        size = int(parts[1]) if len(parts) > 1 and parts[1] else 0
    except ValueError:
        size = 0
    try:
        t = float(parts[2]) if len(parts) > 2 and parts[2] else 0.0
    except ValueError:
        t = 0.0
    # curl reports time in seconds; convert to ms.
    if t and t < 50:
        t = t * 1000.0
    return code, size, t


