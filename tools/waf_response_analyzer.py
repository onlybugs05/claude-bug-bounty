class BaselineSampler:
    """Calibrate Block Baseline (BB) and Normal Baseline (NB) for a target."""

    KNOWN_BAD_PROBES = [
        "/?_waftest=%3Cscript%3Ealert(document.cookie)%3C%2Fscript%3E",
        "/?_waftest=%27%20UNION%20SELECT%20password%20FROM%20users--",
        "/?_waftest=../../../../../../etc/passwd",
        "/?_waftest=%24(id)",
    ]
    NORMAL_PATHS = ["/robots.txt", "/favicon.ico", "/", "/.well-known/security.txt"]

    def __init__(self, db: WAFSignatureDB | None = None) -> None:
        self._db = db or WAFSignatureDB()

    @staticmethod
    def _join(base: str, path: str) -> str:
        if not base.startswith(("http://", "https://")):
            base = "http://" + base
        base = base.rstrip("/")
        # If path starts with "/?" we still concatenate; urljoin would mangle it.
        if path.startswith("/"):
            return base + path
        return base + "/" + path

    def _sample(self, url: str) -> dict[str, Any]:
        resp = _http_get(url, timeout=8.0)
        body = resp.get("body", "")
        headers = resp.get("headers", "")
        vendor_hits = self._db.match_vendors(body, headers)
        return {
            "url": url,
            "status": resp.get("status", 0),
            "length": len(body),
            "elapsed_ms": resp.get("elapsed_ms", 0.0),
            "vendor_hits": vendor_hits,
            "has_block_title": self._db.has_block_title(body),
            "body_sha256": hashlib.sha256(body.encode("utf-8", errors="replace")).hexdigest(),
            "error": resp.get("error"),
        }

    def calibrate(self, base_url: str, output_path: str, quiet: bool = False) -> dict[str, Any]:
        bb_samples: list[dict[str, Any]] = []
        for probe in self.KNOWN_BAD_PROBES:
            url = self._join(base_url, probe)
            if not quiet:
                print(f"[calibrate] BB probe -> {url}", file=sys.stderr)
            bb_samples.append(self._sample(url))

        nb_samples: list[dict[str, Any]] = []
        for path in self.NORMAL_PATHS:
            url = self._join(base_url, path)
            if not quiet:
                print(f"[calibrate] NB probe -> {url}", file=sys.stderr)
            nb_samples.append(self._sample(url))

        # Block baseline summary.
        bb_lengths = [s["length"] for s in bb_samples if s["length"] > 0]
        try:
            bb_median = int(statistics.median(bb_lengths)) if bb_lengths else 0
        except statistics.StatisticsError:
            bb_median = 0
        vendor_counter: dict[str, int] = {}
        for s in bb_samples:
            for v in s["vendor_hits"]:
                vendor_counter[v] = vendor_counter.get(v, 0) + 1
        bb_vendor = max(vendor_counter, key=vendor_counter.get) if vendor_counter else None

        common_sha = ""
        sha_counter: dict[str, int] = {}
        for s in bb_samples:
            sha_counter[s["body_sha256"]] = sha_counter.get(s["body_sha256"], 0) + 1
        if sha_counter:
            common_sha = max(sha_counter, key=sha_counter.get)

        block_baseline = {
            "median_length": bb_median,
            "vendor": bb_vendor,
            "sample_lengths": bb_lengths,
            "common_sha256": common_sha,
            "samples": bb_samples,
        }

        # Normal baseline summary.
        nb_lengths = [s["length"] for s in nb_samples if s["length"] > 0]
        try:
            nb_median = int(statistics.median(nb_lengths)) if nb_lengths else 0
        except statistics.StatisticsError:
            nb_median = 0
        has_framework_signal = any(
            s["status"] in (200, 301, 302, 304) and s["length"] > 0 for s in nb_samples
        )
        normal_baseline = {
            "median_length": nb_median,
            "has_framework_signal": has_framework_signal,
            "samples": nb_samples,
        }

        result = {
            "block_baseline": block_baseline,
            "normal_baseline": normal_baseline,
            "calibrated_at": datetime.now(timezone.utc).isoformat(),
            "base_url": base_url,
        }
        try:
            with open(output_path, "w", encoding="utf-8") as fh:
                json.dump(result, fh, indent=2, ensure_ascii=False)
        except OSError as e:
            print(f"[calibrate] failed to write {output_path}: {e}", file=sys.stderr)
            raise
        if not quiet:
            print(f"[calibrate] wrote {output_path}", file=sys.stderr)
        return result


