def _format_human(result: dict[str, Any]) -> str:
    lines = [
        f"verdict       : {result['verdict']}",
        f"score         : {result['score']}",
        f"status_code   : {result['status_code']}",
        f"body_length   : {result['body_length']}",
        f"bb_length     : {result['block_baseline_length']}",
        f"length_ratio  : {result['length_ratio']}",
        f"waf_vendor    : {result['waf_vendor']}",
        f"is_challenge  : {result['is_challenge']}",
        f"reason        : {result['reason']}",
        "signals       :",
    ]
    for s in result["signals"]:
        lines.append(f"  - {s}")
    lines.append("extracted_ids :")
    for k, v in result["extracted_ids"].items():
        lines.append(f"  - {k}: {v}")
    return "\n".join(lines)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        prog="waf_response_analyzer.py",
        description="Classify HTTP responses as WAF block / bypass / needs review.",
    )
    parser.add_argument("--calibrate", metavar="URL", help="calibrate baseline against URL")
    parser.add_argument("--output", metavar="PATH", help="baseline output path (with --calibrate)")
    parser.add_argument("--quiet", action="store_true", help="suppress progress output")

    parser.add_argument("--classify", action="store_true", help="classify a single response")
    parser.add_argument("--status", type=int, default=0, help="HTTP status code")
    parser.add_argument("--body", metavar="PATH", help="response body file")
    parser.add_argument("--headers", metavar="PATH", help="response headers file (or /dev/null)")
    parser.add_argument("--baseline", metavar="PATH", help="baseline JSON path")
    parser.add_argument(
        "--metrics",
        metavar="CODE|SIZE|TIME",
        help="pipe-delimited metrics from curl (code|size|time)",
    )
    parser.add_argument(
        "--format", choices=["json", "human"], default="human", help="output format"
    )

    parser.add_argument(
        "--diff",
        nargs=2,
        metavar=("BODY_A", "BODY_B"),
        help="compare two body files and print similarity summary",
    )

    args = parser.parse_args(argv)

    db = WAFSignatureDB()
    extractor = LogIDExtractor(db)

    # --diff mode.
    if args.diff:
        a_text = _read_text(args.diff[0])
        b_text = _read_text(args.diff[1])
        report = diff_bodies(a_text, b_text)
        print(json.dumps(report, indent=2, ensure_ascii=False))
        return 0

    # --calibrate mode.
    if args.calibrate:
        if not args.output:
            parser.error("--calibrate requires --output PATH")
        sampler = BaselineSampler(db)
        try:
            sampler.calibrate(args.calibrate, args.output, quiet=args.quiet)
        except Exception as e:  # noqa: BLE001 -- top-level CLI safety
            print(f"[calibrate] error: {e}", file=sys.stderr)
            return 2
        return 0

    # --classify mode.
    if args.classify:
        body = _read_text(args.body)
        headers = _read_text(args.headers)
        baseline = _load_baseline(args.baseline)
        metric_code, metric_size, metric_time = _parse_metrics(args.metrics)

        status_code = args.status or metric_code
        body_length = metric_size if metric_size > 0 else len(body)
        response_time_ms = metric_time

        fp = ResponseFingerprint.build(
            body,
            headers,
            status_code=status_code,
            body_length=body_length,
            response_time_ms=response_time_ms,
            db=db,
            extractor=extractor,
        )
        classifier = ResponseClassifier()
        result = classifier.classify(fp, baseline)

        if args.format == "json":
            print(json.dumps(result, indent=2, ensure_ascii=False))
        else:
            print(_format_human(result))
        return 0

    parser.print_help(sys.stderr)
    return 1


if __name__ == "__main__":
    sys.exit(main())
