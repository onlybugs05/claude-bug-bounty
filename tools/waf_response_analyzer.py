class LogIDExtractor:
    """Extract WAF log/trace identifiers from response body."""

    PATTERNS: dict[str, str] = {
        "cf-ray": r"(?i)(?:cf-ray|cloudflare\s*ray\s*id)[:\s#=]+([0-9a-f]{12,20}(?:-[A-Z]{3})?)",
        "f5-support": r"(?i)support\s*id[:\s#=]+([0-9]{10,30})",
        "akamai-ref": r"(?i)reference\s*#?\s*([0-9a-f.]{6,40})",
        "imperva-incident": r"(?i)incident\s*id[:\s#=]+([0-9\-]{6,40})",
        "aws-request": r"(?i)(?:x-amzn-requestid|request id)[:\s#=]+([A-Z0-9]{16,40})",
        "modsec-rule": r"(?i)\[id\s+\"?([0-9]{4,7})\"?\]",
    }

    def __init__(self, db: WAFSignatureDB) -> None:
        self._db = db
        self._compiled: dict[str, re.Pattern[str]] = {
            k: re.compile(v) for k, v in self.PATTERNS.items()
        }

    def extract(self, body: str) -> dict[str, str | None]:
        out: dict[str, str | None] = {}
        for name, rx in self._compiled.items():
            m = rx.search(body)
            out[name] = m.group(1) if m else None
        out["generic"] = self._db.generic_log_id(body)
        return out


