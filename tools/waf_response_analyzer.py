EMPTY_BASELINE: dict[str, Any] = {
    "block_baseline": {"median_length": 0, "vendor": None, "sample_lengths": [], "common_sha256": ""},
    "normal_baseline": {"median_length": 0, "has_framework_signal": False},
}


class ResponseClassifier:
    """Compute weighted block-score and verdict."""

    THRESHOLD_BLOCKED = 60
    THRESHOLD_REVIEW = 30

    def classify(
        self,
        fp: ResponseFingerprint,
        baseline: dict[str, Any] | None,
    ) -> dict[str, Any]:
        baseline = baseline or EMPTY_BASELINE
        bb = baseline.get("block_baseline") or {}
        bb_len = int(bb.get("median_length") or 0)
        bb_vendor = bb.get("vendor")

        signals: list[dict[str, Any]] = []
        score = 0

        # 1. Vendor body / header match.
        if fp.vendor_hits:
            score += 60
            signals.append(
                {
                    "name": "vendor_body_match",
                    "value": fp.vendor_hits[0],
                    "weight": 60,
                }
            )

        # 2. Generic log-id (incident/reference/...).
        if fp.log_ids.get("generic"):
            score += 25
            signals.append(
                {
                    "name": "generic_log_id",
                    "value": fp.log_ids["generic"],
                    "weight": 25,
                }
            )

        # 3. Title-like block keyword.
        if fp.has_block_title:
            score += 30
            signals.append({"name": "block_title_keyword", "weight": 30})

        # 4. Length similarity to block baseline.
        length_ratio = 0.0
        if bb_len > 0:
            length_ratio = round(fp.body_length / bb_len, 4) if fp.body_length else 0.0
            if fp.body_length == bb_len:
                score += 70
                signals.append(
                    {"name": "length_equals_bb", "value": bb_len, "weight": 70}
                )
            else:
                delta = abs(fp.body_length - bb_len) / max(bb_len, 1)
                if delta <= 0.05:
                    score += 50
                    signals.append(
                        {
                            "name": "length_within_bb_5pct",
                            "value": round(delta, 4),
                            "weight": 50,
                        }
                    )
                elif delta <= 0.15:
                    score += 20
                    signals.append(
                        {
                            "name": "length_within_bb_15pct",
                            "value": round(delta, 4),
                            "weight": 20,
                        }
                    )

        # 4b. Empty body matching an empty-body block baseline (WAF silent-drop).
        _EMPTY_SHA256 = "e3b0c44298fc1c149afbf4c8996fb92427ae41e4649b934ca495991b7852b855"
        bb_sha256 = bb.get("common_sha256", "")
        if fp.body_length == 0 and bb_sha256 == _EMPTY_SHA256:
            score += 70
            signals.append(
                {"name": "empty_body_matches_bb_sha256", "weight": 70}
            )

        # 5. Very fast response time (CDN/WAF edge).
        if 0 < fp.response_time_ms < 80:
            score += 10
            signals.append(
                {"name": "fast_response", "value": fp.response_time_ms, "weight": 10}
            )

        # 6. Absence of business cookies / signals.
        if not fp.has_business_cookies:
            score += 5
            signals.append({"name": "no_business_cookies", "weight": 5})
        if not fp.has_business_signal:
            score += 5
            signals.append({"name": "no_business_signal", "weight": 5})

        # 7. Status-code adjustment: real backend errors lean against "blocked".
        #    These statuses indicate the request reached the backend past the WAF
        #    edge. Treat them as a positive bypass signal only when no vendor
        #    signature has fired — otherwise the vendor signature dominates.
        backend_status_codes = {401, 500, 502, 503}
        if fp.status_code in backend_status_codes:
            if not any(("vendor" in s.get("name", "") or "signature" in s.get("name", "")) for s in signals):
                score += 20
                signals.append({"name": "backend_status_reached", "weight": 20})

        # 8. Challenge-page signal (Cloudflare JS challenge, etc.) -- treat as
        #    blocked but flag separately.
        is_challenge = fp.has_challenge_signal
        if is_challenge and not any(s["name"] == "vendor_body_match" for s in signals):
            score += 30
            signals.append({"name": "challenge_signal", "weight": 30})

        # Verdict.
        if score >= self.THRESHOLD_BLOCKED:
            verdict = "blocked"
        elif score >= self.THRESHOLD_REVIEW:
            verdict = "needs_review"
        else:
            verdict = "bypassed"

        # Reason -- human readable.
        reason_parts: list[str] = []
        if fp.vendor_hits:
            reason_parts.append(f"matched vendor signature ({', '.join(fp.vendor_hits)})")
        if bb_len > 0 and length_ratio:
            reason_parts.append(
                f"body length {fp.body_length} vs block baseline {bb_len} (ratio {length_ratio})"
            )
        if fp.has_block_title:
            reason_parts.append("title contains block keyword")
        if fp.log_ids.get("generic"):
            reason_parts.append(f"log id detected ({fp.log_ids['generic']})")
        if is_challenge:
            reason_parts.append("challenge-page signal present")
        if fp.status_code in {401, 500, 502, 503}:
            reason_parts.append(f"status {fp.status_code} suggests backend response")
        if not reason_parts:
            reason_parts.append("no strong block signals detected")

        # Pick best vendor: explicit hits beat baseline-derived.
        waf_vendor = fp.vendor_hits[0] if fp.vendor_hits else bb_vendor

        return {
            "verdict": verdict,
            "score": score,
            "reason": "; ".join(reason_parts),
            "status_code": fp.status_code,
            "body_length": fp.body_length,
            "block_baseline_length": bb_len,
            "length_ratio": length_ratio,
            "waf_vendor": waf_vendor,
            "is_challenge": is_challenge,
            "signals": signals,
            "extracted_ids": fp.log_ids,
            "body_preview": fp.body_preview,
            "body_sha256": fp.body_sha256,
        }


